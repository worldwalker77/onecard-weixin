package cn.worldwalker.onecard.weixin.domain;

public class ModifyParam {
	private String oldIdNum;
	private String oldTel;
	private String newIdNum;
	private String newTel;
	public String getOldIdNum() {
		return oldIdNum;
	}
	public void setOldIdNum(String oldIdNum) {
		this.oldIdNum = oldIdNum;
	}
	public String getOldTel() {
		return oldTel;
	}
	public void setOldTel(String oldTel) {
		this.oldTel = oldTel;
	}
	public String getNewIdNum() {
		return newIdNum;
	}
	public void setNewIdNum(String newIdNum) {
		this.newIdNum = newIdNum;
	}
	public String getNewTel() {
		return newTel;
	}
	public void setNewTel(String newTel) {
		this.newTel = newTel;
	}
	
}
